"""
需求：99乘法表

分析：
    外层循环 i = 1 i <= 9
    i = 1
        内层循环 j = 1 j<=i
        1*1 = 1

        1*2 = 2 2*2 = 4

        j * i =
        1 * 1 = 1
    i = 2
        内层循环 j = 1 j<=i
        1 * 2 = 2
        2 * 2 = 4
    i = 3


"""

i = 1
#外层循环控制行数 i = 1 i <= 9
while i <= 9:
    j = 1
    #内层循环控制每行打印的个数 j = 1 j<=i
    while j <= i:
        print("%d * %d = %d" % (j,i,j*i),end='\t')
        j += 1
    print()
    i += 1

